            1985-1987: The Ironhead Origins
        </h2>
        <div class="row align-items-center">
            <div class="col-md-8">
                <p class="fs-5 text-muted">
                    The story begins at <strong>Commonwealth Polytechnic Institute (CPI)</strong> in Blacksburg, Virginia. Through a combination of delayed entry (Ryan) and academic acceleration (Holly), all five family members—siblings Ryan, Cassidy, and Holly O'Connell, and their cousins Evan and Tyler Wright—managed to enter the <strong>Class of '89</strong> together.
                </p>
                <p>
                    This wasn't just about school; it was about survival. Ryan and Cassidy, both navigating the world with autism, relied on each other (and their sister Holly) as their designated "Safe Person." The five of them formed a protective, insular unit—a "fortress" long before they had a name for it.
                </p>
                <p>
                    They weren't just students; they were "Ironheads." In 1986, they wrote <strong>"Ignition (The Forger's Call),"</strong> an industrial-rock anthem donated to the university's football team. When 60,000 fans at "The Crucible" adopted it as their sacred entrance music, the band became local legends before they ever signed a contract.
                </p>
            </div>
            <div class="col-md-4 text-center">
                <i class="fa-duotone fa-graduation-cap fa-5x opacity-25"></i>
            </div>
        </div>
    </section>

    <section id="apex-years" class="mb-5">
        <h2 class="text-primary border-bottom border-primary pb-2 mb-4">
            1987-1992: The Apex "Cold War"
        </h2>
        <div class="alert alert-light border-secondary">
            <i class="fa-duotone fa-circle-info me-2"></i>
            <strong>Key Albums:</strong> <a href="/discography/1987-electric-color">Electric Color</a>, <a href="/discography/1989-neon-hearts">Neon Hearts</a>
        </div>
        <p class="fs-5 text-muted">
            Their success at CPI attracted <strong>Apex Records</strong>, a major label looking for the next big synth-pop act. This began a five-year "Cold War" over the band's identity.
        </p>
        <p>
            Apex created reductive personas: Cassidy was "The Stardust" (the pop princess) and Ryan was "The Engine" (the rock grunt). The label actively suppressed their heavier sound, leading to a series of albums defined by "malicious compliance," where the band would hide anti-corporate lyrics inside polished pop songs like <em>Plastic Sky</em> and <em>Hollow Sound</em>.
        </p>
    </section>

    <section id="independence" class="mb-5">
        <h2 class="text-warning border-bottom border-warning pb-2 mb-4">
            1992: The "Friction" Catalyst
        </h2>
        <div class="card border-warning bg-transparent">
            <div class="card-body">
                <h3 class="h5 fw-bold text-warning">The Day The Contract Ended</h3>
                <p>
                    In 1992, during pre-production for their third album, <em>Friction</em>, the conflict with Apex reached a breaking point. A new executive made a "fatal assumption" about the band's dynamic. He mistook the profound, comfortable intimacy between Ryan and Cassidy—born of a lifetime of caregiving and being each other's "Safe Person"—for something romantic.
                </p>
                <p>
                    He attempted to push a marketing narrative that sexualized their bond, violating their integrity as siblings in the most offensive way possible. The result was explosive. <strong>Holly O'Connell</strong> (then a law student) and <strong>Ryan O'Connell</strong> drew a line in the sand. They didn't just reject the marketing; they "nuked" the relationship.
                </p>
                <p class="mb-0">
                    Apex Records, facing a legal and PR nightmare, voided the contract and surrendered the master tapes. The band was free, but they were effectively blacklisted and broke.
                </p>
            </div>
        </div>
    </section>

    <section id="freedom-era" class="mb-5">
        <h2 class="text-primary border-bottom border-primary pb-2 mb-4">
            1995-2005: The Freedom Era
        </h2>
        <div class="row">
            <div class="col-md-12">
                <p class="fs-5 text-muted">
                    Penniless but free, the band retreated to a warehouse in Blacksburg they dubbed "The Fortress." They were saved by the <strong>"Forger Nation War Chest,"</strong> a grassroots fundraising campaign by their loyal college fanbase.
                </p>
                <p>
                    Holly founded <strong>Engine Room Records, LLC</strong>, and the band began their most prolific period.
                </p>
                <ul>
                    <li class="mb-2"><strong>1995:</strong> Released <em>The Warehouse Tapes</em>, a raw, defiant EP that sounded like 80s arena rock dropped into the middle of the 90s Grunge era.</li>
                    <li class="mb-2"><strong>1997:</strong> Released <em>Hard Reset</em>, their professional comeback and a critical darling.</li>
                    <li class="mb-2"><strong>2000-2005:</strong> They toured relentlessly, releasing <em>Signal & Noise</em> and <em>The Long Way Home</em>, playing for the fans who saved them.</li>
                </ul>
            </div>
        </div>
    </section>

    <section id="hiatus" class="mb-5">
        <h2 class="border-bottom border-secondary pb-2 mb-4">
            2008-2014: The Long Hiatus
        </h2>
        <p class="fs-5 text-muted">
            After a decade of non-stop independent touring, the "family unit" needed to heal. In 2008, following the grueling "Long Way Home" tour, the band announced an indefinite hiatus.
        </p>
        <p>
            They didn't break up—they just went home. They retreated completely from the public eye, locking the doors of "The Fortress" to recover from the burnout of the industry. They spent these years simply existing as brothers, sisters, and cousins, reclaiming the family bond that the business had tried to commodify.
        </p>
        <p>
             During this silence, they released <em>Lost Sounds</em> (2007), a compilation of polished demos from the Apex era, as a gift to keep the fans engaged while they slept.
        </p>
    </section>

    <section id="homecoming" class="mb-5">
        <h2 class="text-glow-primary border-bottom border-primary pb-2 mb-4">
            2015-2016: Re-Ignition
        </h2>
        
        <p class="fs-5 text-muted">
            The silence broke in 2015 with the release of <strong>Re-Ignition</strong>. The album was a massive return to form, featuring the "Stardust" melodies and "Engine" power that defined them.
        </p>
        <p>
            This tour culminated in the 2016 live album <strong>Live at The Crucible</strong>—a homecoming show at CPI's stadium where they played "Ignition" live for the first time in decades, accompanied by the university marching band.
        </p>
    </section>

