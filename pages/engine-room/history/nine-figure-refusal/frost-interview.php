?>

<div class="container py-5">
    
    <div class="row justify-content-center mb-5">
        <div class="col-lg-8 text-center">
            <span class="badge bg-info text-dark rounded-pill px-3 py-2 mb-3 text-uppercase letter-spacing-1 shadow-glow border border-info">
                <i class="fa-duotone fa-user-clock me-2"></i>Epilogue: February 14, 2019
            </span>
            <h1 class="display-4 fw-bold text-white mb-2" style="font-family: 'Impact', sans-serif;">
                THE INTERVIEW
            </h1>
            <p class="lead text-muted font-monospace">
                "We are looking for someone who understands that people are not line items."
            </p>
        </div>
    </div>

    <div class="row justify-content-center mb-5">
        <div class="col-lg-10">
            
            <div class="card border-0 shadow-lg">
                <div class="card-body p-5 font-serif">
                    
                    <h5 class="text-uppercase text-muted fw-bold mb-4 border-bottom pb-2">
                        Location: The Jessica Miller Center (Former Omni-Global HQ)
                    </h5>

                    <div class="p-4 bg-secondary bg-opacity-10 border-start border-4 border-secondary mb-4 fst-italic">
                        <p class="mb-0">
                            <strong>Context:</strong> Following the collapse of Omni-Global, Jameson Frost was terminated for "Gross Negligence" regarding the Due Diligence of the Engine Room acquisition. Blacklisted by Wall Street, he applied for a mid-level analyst role at the newly formed "Engine Room Philanthropies."
                        </p>
                    </div>

                    <p>
                        Jameson Frost adjusted his silk tie. He checked his reflection in the glass of the waiting room door. Perfect. He looked like money. He looked like he belonged in the C-Suite, not sitting in a lobby waiting for a "Junior Logistics Coordinator" interview.
                    </p>
                    
                    <p>
                        <em>Just get the foot in the door,</em> he told himself. <em>Dazzle them with the resume. Run the place in six months.</em>
                    </p>

                    <p class="mt-4">
                        A flat, synthesized electronic voice cut through his thoughts.
                    </p>
                    <p class="fw-bold text-primary">
                        "Mr. Frost?"
                    </p>

                    <p>
                        Frost turned. A young man was standing there, holding a tablet with a rugged protective case. He was wearing faded jeans and a black t-shirt featuring a neon blue rocket inside a gear.
                    </p>

                    <p>
                        Frost sighed internally. <em>IT support,</em> he thought, eyeing the device. <em>Probably here to fix the wi-fi.</em>
                    </p>

                    <p>
                        "I'm here to see the Director, son," Frost said dismissively, checking his Rolex. "Is she in?"
                    </p>

                    <p>
                        <strong>Justin Miller</strong> didn't react to the tone. He simply tapped an icon on his screen.
                    </p>

                    <p class="fw-bold text-primary">
                        "Follow. Me."
                    </p>

                    <p>
                        "Right. Thanks," Frost muttered. He brushed past Justin, walking briskly toward the double doors. "I can find my own way."
                    </p>
                    
                    <hr class="my-5 border-secondary opacity-25">

                    <p>
                        Frost pushed open the heavy oak doors of the corner office—<em>his</em> old office. The view of Los Angeles was just as majestic as he remembered.
                    </p>

                    <p>
                        Sitting behind the massive glass desk was not a corporate recruiter.
                    </p>

                    <p>
                        It was a young woman in a wheelchair. She was wearing a band t-shirt. She looked up from a stack of files—<em>his</em> personnel files from the acquisition attempt.
                    </p>

                    <div class="card border-0 shadow-lg mb-4 mt-4 bg-body-tertiary">
                        <img src="https://assets.raggiesoft.com/engine-room-records/images/omni-global/frost-interview.jpg" 
                             class="card-img-top" 
                             alt="Jessica Miller sitting confidently behind the massive glass executive desk, while Jameson Frost sits uncomfortably in a low visitor chair."
                             style="filter: sepia(0.2) contrast(1.1); max-height: 500px; object-fit: cover; object-position: top;">
                        
                        <div class="card-footer bg-black text-white fst-italic small text-center p-3">
                            <div class="d-flex align-items-center justify-content-center">
                                <i class="fa-solid fa-quote-left text-muted opacity-25 fa-2x me-3"></i>
                                <div>
                                    "The view from this side of the desk is different, isn't it Jameson? <br>
                                    This time, <strong>I</strong> ask the questions."
                                </div>
                                <i class="fa-solid fa-quote-right text-muted opacity-25 fa-2x ms-3"></i>
                            </div>
                        </div>
                    </div>

                    <p>
                        Frost froze. The memory of the "Bus Memo" flashed through his mind. The intern. The girl he had mocked for using a wheelchair as a walker.
                    </p>

                    <p>
                        "Ms... Ms. Miller?" Frost stammered.
                    </p>

                    <p>
                        <strong>Jessica</strong> smiled. It wasn't a warm smile.
                    </p>

                    <p>
                        "Have a seat, Jameson," she said.
                    </p>

                    <p>
                        The door opened behind him. Justin walked in, closing the door with a practiced maneuver. He stood next to his sister. He tapped the screen again.
                    </p>

                    <p class="fw-bold text-primary">
                        "Wi-fi. Is. Working. Fine."
                    </p>

                    <p>
                        Jessica folded her hands over Frost's resume.
                    </p>

                    <p>
                        "My Chief Financial Officer tells me you have experience with... 'aggressive valuations,'" Jessica said, glancing at Justin. "But we're concerned about your attention to detail. Specifically regarding people you assume aren't important."
                    </p>

                    <p>
                        She slid a piece of paper across the desk. It was a printout of the Bus Memo.
                    </p>

                    <p>
                        "Walk us through this, Mr. Frost. Convince us you've changed."
                    </p>

                    <p>
                        Frost picked up the paper. His hands weren't shaking—he was too proud for that—but his throat was dry. He cleared it.
                    </p>

                    <p>
                        "You have to understand the... ecosystem I came from," Frost began, his voice finding its old boardroom rhythm. "In M&A, we don't look at people. We look at leverage. We look for fracture points."
                    </p>

                    <p>
                        He gestured vaguely at Jessica's chair, then at Justin's tablet.
                    </p>

                    <p>
                        "I saw a fracture point. It was a miscalculation of asset resilience, yes. But it wasn't personal. It was... quantitative. I was trying to save my shareholders money. That's the job. That's the shark tank."
                    </p>

                    <p>
                        He leaned forward, thinking he had found the angle.
                    </p>

                    <p>
                        "And that's why you need me," Frost pressed, his confidence returning. "You're running a massive endowment now. You're swimming with the same sharks I used to swim with. You need a killer at the gate. You need someone who knows how to look at a... at a 'fracture point'... and exploit it before they exploit you."
                    </p>

                    <p>
                        Jessica stared at him. "You think our disability is a fracture point."
                    </p>

                    <p>
                        "In the market? Yes," Frost said, thinking he was being helpful. "It's an optical liability. Donors get skittish around... medical complexity. They want strength. I can help you manage that image. I can help you polish the narrative so you look..."
                    </p>

                    <p>
                        He searched for the word. He found the wrong one.
                    </p>

                    <p>
                        "...Capable."
                    </p>

                    <p>
                        The silence in the room was absolute. The only sound was the hum of the air conditioning and the faint <em>tap-tap-tap</em> of Justin's finger on his screen.
                    </p>

                    <p>
                        Jessica didn't get angry. She didn't yell. She simply closed the file folder.
                    </p>

                    <p>
                        "Mr. Frost," Jessica said softly. "We aren't hiring a shark. We're building a sanctuary."
                    </p>

                    <p>
                        "I'm offering you protection!" Frost argued, sensing the ground slipping away. "I'm the only one here who knows how the real world works!"
                    </p>

                    <p>
                        Justin stopped tapping. He turned his tablet toward Frost and pressed the 'Speak' button. The volume was set to maximum.
                    </p>

                    <div class="alert alert-dark border-start border-4 border-primary bg-black text-white p-4 my-4 shadow-sm">
                        <i class="fa-solid fa-volume-high me-2 text-primary"></i>
                        <span class="font-monospace fw-bold text-uppercase" style="letter-spacing: 2px;">
                            "We. Do. Not. Fix. People. We. Fix. The. System."
                        </span>
                    </div>

                    <p>
                        Jessica pointed to the door.
                    </p>

                    <p>
                        "The interview is over, Jameson. Justin will validate your parking. It's the least we can do."
                    </p>

                    <p>
                        Frost stood up, his face flushing red. " You're making a mistake. You're going to get eaten alive out there without me."
                    </p>

                    <p>
                        "We already survived you," Jessica replied, turning her chair back to her work. "I think we'll be fine."
                    </p>

                    <p>
                        Jameson Frost walked out of the office, past the logo on the glass wall—a rocket inside a gear—and into a world that had moved on without him.
                    </p>

                </div>
